    private void performEvent() {
        update.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if(checkInput() == false){
                    return;
                }
                NhanVien nv = new NhanVien(
                        id.getText(),
                        name.getText(),
                        LocalDate.parse(date.getText(),DateTimeFormatter.ofPattern("yyyy-MM-dd")),
                        address.getText(),
                        phone.getText(),
                        user.getText(),
                        pass.getText(),
                        (right.getSelectedIndex() == 0) ? "Admin":"NhanVien",
                        true
                );
                
                if(nvbus.addNhanVien(nv) == true){
                    JOptionPane.showMessageDialog(null, "Thêm thành công");
                    reset();
                    return;
                }
                JOptionPane.showMessageDialog(null, "Không thể thêm");
            }
        });
    }

    private void loadConboBox() {
        right.addItem("Admin");
        right.addItem("Nhân Viên");
    }

    private void reset() {
       id.setText("");
       name.setText("");
       date.setText("");
       address.setText("");
       phone.setText("");
       user.setText("");
       pass.setText("");
       right.setSelectedIndex(0);
    }
    
    private boolean checkInput(){
        if(id.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập mã Nhân Viên");
            return false;
        }
        if(nvbus.isExistId(id.getText())){
             JOptionPane.showMessageDialog(null, "Mã nhân viên đã tồn tại");
            return false;
        }
        if(name.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập tên Nhân Viên");
            return false;
        }
        if(date.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập ngày sinh Nhân Viên");
            return false;
        }
        try {
            LocalDate.parse(date.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ngày sinh Nhân Viên phải có dạng yyyy-mm-dd");
            return false;
        }
        
        if(address.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ Nhân Viên");
            return false;
        }
        if(phone.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập SĐT Nhân Viên");
            return false;
        }
        if(phone.getText().length() != 10){
            JOptionPane.showMessageDialog(null, "SĐT phải có 10 số");
            return false;
        }
        try {
            Integer.valueOf(phone.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "SĐT không hợp lệ");
            return false;
        }
        if(user.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập Tên đăng nhập Nhân Viên");
            return false;
        }
        if(nvbus.isExistUser(user.getText())){
            JOptionPane.showMessageDialog(null, "Tên đăng nhập Nhân Viên đã được sử dụng");
            return false;
        }
        if(pass.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập SĐT Mật khẩu");
            return false;
        }
        if(ChucNang.isValidPass(pass.getText()) == false){
            JOptionPane.showMessageDialog(null, "Mật khẩu không hợp lệ. Mật khẩu chỉ chứa chữ thường, chữ in hoa và số");
            return false;
        }
        return true;
    }
